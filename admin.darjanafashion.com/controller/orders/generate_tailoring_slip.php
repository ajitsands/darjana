class TailoringSlipPDF extends TCPDF {
    public function Header() {
        // Logo on the left - increased size
        $image_file = 'https://admin.darjanafashion.com/assets/img/illustrations/shopping_logo.png';
        $this->Image($image_file, 15, 12, 40, 0, 'PNG', '', 'T', false, 300, '', false, false, 0, false, false, false);
        
        // Company name - perfectly centered on the page
        $this->SetFont('helvetica', 'B', 18);
        
        // Save current position
        $currentY = $this->GetY();
        
        // Calculate center position (page width/2)
        $pageWidth = $this->getPageWidth();
        $textWidth = $this->GetStringWidth('DARJANA FASHION');
        $centerX = ($pageWidth - $textWidth) / 2;
        
        // Set position to exact center
        $this->SetXY($centerX, 15);
        $this->Cell($textWidth, 10, 'DARJANA FASHION', 0, 1, 'C');
        
        // Add line under DARJANA FASHION
        $this->SetLineWidth(0.5);
        $this->SetDrawColor(120, 120, 120);
        $this->Line(15, $this->GetY(), $pageWidth - 15, $this->GetY());
        
        // Add some spacing after the line
        $this->SetY($this->GetY() + 5);
    }

    public function Footer() {
        $this->SetY(-18);
        $this->SetFont('helvetica', 'I', 8);
        $this->SetTextColor(120, 120, 120);
        $this->Cell(0, 10, 'Tailoring Slip | Generated by Darjana Fashion Tailoring System', 0, 0, 'C');
        $this->Cell(0, 10, 'Page '.$this->getAliasNumPage().'/'.$this->getAliasNbPages(), 0, 0, 'R');
    }
}


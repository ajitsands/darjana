class TailoringSlipGenerator {
    private $batch_id;
    private $conn;
    private $batch;
    private $items;
    private $tailoring_unit;

    public function __construct($conn) {
        $this->batch_id = isset($_GET['batch_id']) ? (int)$_GET['batch_id'] : 0;
        
        if ($this->batch_id <= 0) {
            die('Error: Invalid batch ID');
        }
        $this->conn = $conn;
        $this->fetchBatchDetails();
    }

    private function fetchBatchDetails() {
        // ── Batch + Unit ───────────────────────────────────────
        $query = "
            SELECT
                tb.*,
                tu.unit_name,
                tu.contact_person,
                tu.phone,
                tu.email,
                tu.address
            FROM tailoring_batches tb
            JOIN tailoring_units tu ON tb.tailoring_unit_id = tu.ids
            WHERE tb.batch_id = ?";
        
        $stmt = $this->conn->prepare($query);
        $stmt->bind_param('i', $this->batch_id);
        $stmt->execute();
        $result = $stmt->get_result();
        
        if ($result->num_rows === 0) {
            die('Error: Batch not found');
        }
        
        $this->batch = $result->fetch_assoc();
        
        $this->tailoring_unit = [
            'name'          => $this->batch['unit_name'],
            'contact_person'=> $this->batch['contact_person'],
            'phone'         => $this->batch['phone'],
            'email'         => $this->batch['email'],
            'address'       => $this->batch['address'],
            'description'   => $this->batch['description']
        ];

        // ── Items ──────────────────────────────────────────────
        $itemQuery = "
            SELECT
                tbi.*,
                od.order_id,
                od.product_name,
                od.quantity,
                od.product_color as color,
                od.product_size as size,
                od.product_length as length,
                od.product_notes as notes
            FROM tailoring_batch_items tbi
            JOIN order_details od ON tbi.order_detail_id = od.ids
            WHERE tbi.batch_id = ?
            ORDER BY od.order_id";
        
        $itemStmt = $this->conn->prepare($itemQuery);
        $itemStmt->bind_param('i', $this->batch_id);
        $itemStmt->execute();
        $result = $itemStmt->get_result();
        
        $this->items = [];
        while ($row = $result->fetch_assoc()) {
            $this->items[] = $row;
        }
        
        if (empty($this->items)) {
            die('Error: No items found in this batch');
        }
    }

    public function generateSlip() {
        $pdf = new TailoringSlipPDF(PDF_PAGE_ORIENTATION, PDF_UNIT, PDF_PAGE_FORMAT, true, 'UTF-8', false);
        
        $pdf->SetCreator('Darjana Fashion');
        $pdf->SetAuthor('Darjana Fashion');
        $pdf->SetTitle('Tailoring Slip - ' . $this->batch['batch_code']);
        
        // Margins: increased top margin for larger logo
        $pdf->SetMargins(15, 40, 15); // Increased from 35 to 40
        $pdf->SetAutoPageBreak(true, 20);
        
        $pdf->AddPage();
        
        $html = $this->generateHTML();
        $pdf->writeHTML($html, true, false, true, false, '');
        
        // Output
        header('Content-Type: application/pdf');
        header('Content-Disposition: inline; filename="tailoring_slip_' . $this->batch['batch_code'] . '.pdf"');
        header('Cache-Control: private, max-age=0, must-revalidate');
        header('Pragma: public');
        
        $pdf->Output('tailoring_slip_' . $this->batch['batch_code'] . '.pdf', 'I');
        exit;
    }

